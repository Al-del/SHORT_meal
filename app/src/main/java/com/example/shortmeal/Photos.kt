private fun takePhoto(
    context: Context,
    filenameFormat: String,
    imageCapture: ImageCapture,
    outputDirectory: File,
    executor: Executor,
    onImageCaptured: (Uri) -> Unit,
    onError: (ImageCaptureException) -> Unit,
    user: String
) {

    val photoFile = File(
        outputDirectory,
        SimpleDateFormat(filenameFormat, Locale.US).format(System.currentTimeMillis()) + ".jpg"
    )

    val outputOptions = ImageCapture.OutputFileOptions.Builder(photoFile).build()

    imageCapture.takePicture(outputOptions, executor, object: ImageCapture.OnImageSavedCallback {
        override fun onError(exception: ImageCaptureException) {
            Log.e("kilo", "Take photo error:", exception)
            onError(exception)
        }

        override fun onImageSaved(outputFileResults: ImageCapture.OutputFileResults) {
            val savedUri = Uri.fromFile(photoFile)
            Log.i("kilo", "Image saved: $savedUri")
            onImageCaptured(savedUri)
            val imageBitmap = BitmapFactory.decodeFile(savedUri.path)
            //print the shape of the image
            Log.i("kilo", "Image saved: ${imageBitmap.width} x ${imageBitmap.height}")
            val clasS=ImageClassifier(context)
         var class_pred=clasS.classify(imageBitmap)
             //Go to Recepies activity
            val intent = Intent(context, Recepies::class.java)
            intent.putExtra("recepie", class_pred)
            intent.putExtra("username", user)
            context.startActivity(intent)

        }
    })
}
private suspend fun Context.getCameraProvider(): ProcessCameraProvider = suspendCoroutine { continuation ->
    ProcessCameraProvider.getInstance(this).also { cameraProvider ->
        cameraProvider.addListener({
            continuation.resume(cameraProvider.get())
        }, ContextCompat.getMainExecutor(this))
    }
}
@Composable
fun CameraView(
    outputDirectory: File,
    executor: Executor,
    onImageCaptured: (Uri) -> Unit,
    onError: (ImageCaptureException) -> Unit,
    username: String
) {
    // 1
    val lensFacing = CameraSelector.LENS_FACING_BACK
    val context = LocalContext.current
    val lifecycleOwner = LocalLifecycleOwner.current
    val preview = Preview.Builder().build()
    val previewView = remember { PreviewView(context) }
    val imageCapture: ImageCapture = remember { ImageCapture.Builder().build() }
    val cameraSelector = CameraSelector.Builder()
        .requireLensFacing(lensFacing)
        .build()
    LaunchedEffect(lensFacing) {
        val cameraProvider = context.getCameraProvider()
        cameraProvider.unbindAll()
        cameraProvider.bindToLifecycle(
            lifecycleOwner,
            cameraSelector,
            preview,
            imageCapture
        )

        preview.setSurfaceProvider(previewView.surfaceProvider)
    }

    Box(contentAlignment = Alignment.BottomCenter, modifier = Modifier.fillMaxSize()) {
        AndroidView({ previewView }, modifier = Modifier.fillMaxSize())

        IconButton(
            modifier = Modifier.padding(bottom = 20.dp),
            onClick = {
                Log.i("kilo", "ON CLICK")
                takePhoto(
                    context = context,
                    filenameFormat = "yyyy-MM-dd-HH-mm-ss-SSS",
                    imageCapture = imageCapture,
                    outputDirectory = outputDirectory,
                    executor = executor,
                    onImageCaptured = onImageCaptured,
                    onError = onError,
                    user=username
                )
            },
            content = {
                Icon(
                    imageVector = Icons.Sharp.Lens,
                    contentDescription = "Take picture",
                    tint = Color.White,
                    modifier = Modifier
                        .size(100.dp)
                        .padding(1.dp)
                        .border(1.dp, Color.White, CircleShape)
                )
            }
        )
    }
}

fun uploadImageToServer(imageUri: Uri, serverUrl: String) {
    val client = OkHttpClient()

    // Convert the Uri to a File object
    val imageFile = File(imageUri.path)

    // Create a RequestBody instance from the image file
    val requestBody = imageFile.asRequestBody("image/*".toMediaTypeOrNull())

    // Create a MultipartBody.Part instance from the RequestBody
    val multipartBodyPart = MultipartBody.Part.createFormData("image", imageFile.name, requestBody)

    // Create a MultipartBody and add the MultipartBody.Part to it
    val multipartBody = MultipartBody.Builder()
        .setType(MultipartBody.FORM)
        .addPart(multipartBodyPart)
        .build()

    // Build the request
    val request = Request.Builder()
        .url(serverUrl)
        .post(multipartBody)  // Pass the MultipartBody to the post method
        .build()

    // Execute the request
    client.newCall(request).execute().use { response ->
        if (!response.isSuccessful) throw IOException("Unexpected code $response")
        println(response.body?.string())
    }
}
